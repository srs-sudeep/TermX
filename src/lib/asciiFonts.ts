export function buildBanner(text: string): string {
  const upper = text.toUpperCase();
  const rows: string[][] = [[], [], [], [], []];
  const fallback = FONT['?'];

  for (const char of upper) {
    const glyph: Glyph = FONT[char] ?? fallback;
    for (let r = 0; r < 5; r++) {
      rows[r].push(glyph[r]);
    }
  }

  return rows.map((row) => row.join('')).join('\n');
}
